Las tareas sobre las que se pueden conceder o denegar permisos son las siguientes: 
* CONNECT Controla la capacidad de conectarse a la base de datos.
* SELECT Permite seleccionar (consultar) datos de la base de datos.
* INSERT Permite agregar nuevos registros o datos a la base de datos.
* UPDATE Permite modificar (actualizar) registros o datos existentes en la base de datos.
* DELETE Permite eliminar registros o datos de la base de datos.
* USAGE Otorga derechos de uso genéricos en una base de datos.

Admite las siguientes sentencias de definición:
* CREATE Se utiliza para crear tablas, bases de datos y otros objetos en la base de datos.
* DROP Permite eliminar tablas, bases de datos y otros objetos de la base de datos.
* ALTER Usado para modificar la estructura de las tablas y otros objetos en la base de datos.

Su misión es la manipulación de datos. A través de él podemos seleccionar, insertar, eliminar y actualizar datos. Utilizaremos por lo general los siguientes comandos:
* SELECT Se utiliza para recuperar datos de la base de datos.
* INSERT  Permite agregar nuevos datos o registros a una tabla.
* UPDATE Se usa para modificar datos existentes en una tabla.
* DELETE Permite eliminar datos o registros de una tabla